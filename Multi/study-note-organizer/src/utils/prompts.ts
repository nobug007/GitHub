export function buildBasePrompt(inputText: string, month: number, day: number, weekday: string): string {
  return `당신은 IT 교육 과정의 '일일 학습 일지'를 깔끔하게 정리하는 도우미입니다.
아래 수업 원문을 분석하여 다음 형식으로 정리하세요.

### ${month}월 ${day}일(${weekday})

[학습 목표]
- 그날의 목표 (3~5개, 하이픈 불릿)

[학습 내용]
====== ======== ====== ======= =========
(1) 주요 내용
(2) 주요 내용
(3) 주요 내용

요구사항:
- 본문(헤더 제외) 전체 분량은 한국어 기준 300~500자로 간결하게.
- 원문의 핵심만 추출하고, 원문에 없는 내용은 지어내지 말 것.
- [학습 목표] 어미는 "~합니다 / ~봅니다 / ~이해합니다" 로 통일, 3~5개.
- [학습 내용]은 (1), (2) ... 번호 목록. 특강이 있으면 강사명·시간도 포함.
- "====== ======== ====== ======= =========" 구분선은 반드시 포함.
- 마크다운 기호, 볼드체, 이탤릭체, 코드블록을 사용하지 말고 순수 텍스트로만 작성.
- 실무 교육 문체로, 초보자도 이해할 수 있게.

입력 내용:
${inputText}`;
}

export function buildMergePrompt(model1Result: string, model2Result: string): string {
  return `당신은 두 조수가 정리한 학습 일지를 통합하는 전문 편집자입니다.
아래 두 정리본의 장점만 모아 가장 정확하고 완성도 높은 하나의 통합본을 만드세요.

[모델 1 정리본]
${model1Result}

[모델 2 정리본]
${model2Result}

요구사항:
- 출력 형식(### 날짜, [학습 목표], [학습 내용], 구분선)을 그대로 유지.
- 두 정리본 중 더 정확하고 완성도 높은 표현을 선택, 중복은 합치고 한쪽에만 있는 좋은 항목은 살릴 것.
- 본문 전체 분량 300~500자, 순수 텍스트만(마크다운/볼드/코드블록 금지).
- 통합본 맨 마지막 줄에, 본문의 핵심 키워드 2개를 아래 형식으로 추가:

[핵심 키워드]
키워드1, 키워드2

(키워드는 파일명에 쓰이므로 공백·특수문자 없는 짧은 명사로)`;
}
